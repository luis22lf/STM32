#define BUFFER_SIZE 64       // Tamanho total do buffer circular
#define MSG_MAX_SIZE 50      // Tamanho máximo esperado de uma mensagem

uint8_t rxBuffer[BUFFER_SIZE];  // Buffer circular para armazenamento de dados
volatile uint8_t rxIndex = 0;   // Índice atual no buffer circular
uint8_t rxChar = 0;


void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
    if (huart->Instance == USART1) {
        if (rxIndex < BUFFER_SIZE - 1) {
            // Armazena o caractere recebido no buffer e incrementa o índice
            rxBuffer[rxIndex++] = rxChar;
        }

        // Verifica se o caractere recebido é um delimitador (neste caso, '\n') indicando o fim da mensagem
        if (rxChar == '\n' || rxIndex >= BUFFER_SIZE - 1) {
            rxBuffer[rxIndex] = '\0'; // Adiciona um caractere nulo ao final da mensagem
            processMessage(); // Chama o processamento da mensagem imediatamente
            rxIndex = 0;        // Reinicia o índice do buffer
        }

        // Reinicia a recepção de um novo caractere
        HAL_UART_Receive_IT(&huart1, &rxChar, 1);
    }
}




void processMessage(void) {
    uint8_t message[BUFFER_SIZE];  // Buffer temporário
    uint8_t messageLen = 0;

    // Copia o conteúdo do buffer para o buffer temporário
    for (int i = 0; i < BUFFER_SIZE && rxBuffer[i] != '\0'; i++) {
        message[messageLen++] = rxBuffer[i];
    }

    // Transmissão não bloqueante
    HAL_UART_Transmit_IT(&huart1, message, messageLen);
}




